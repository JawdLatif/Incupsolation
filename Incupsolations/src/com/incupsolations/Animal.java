package com.incupsolations;

public class Animal {

	private String name;
	private String evrageWight;
	private String numberLeg;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEvrageWight() {
		return evrageWight;
	}
	public void setEvrageWight(String evrageWight) {
		this.evrageWight = evrageWight;
	}
	public String getNumberLeg() {
		return numberLeg;
	}
	public void setNumberLeg(String numberLeg) {
		this.numberLeg = numberLeg;
	}
	
	
}
